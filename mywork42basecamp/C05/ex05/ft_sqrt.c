int	ft_sqrt(int nb)
{
	int	i;
	int	j;

	i = 1;
	j = nb / i;
	while (i <= j)
	{
		if (i * i == nb)
		{
			return (i);
		}
		j = nb / ++i;
	}
	return (0);
}
