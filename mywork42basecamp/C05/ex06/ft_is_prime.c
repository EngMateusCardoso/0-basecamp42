int	ft_is_prime(int nb)
{
	int	i;
	int	j;

	i = 2;
	j = nb / i;
	if (nb < 2)
		return (0);
	while (i <= j)
	{
		if (nb % i == 0)
			return (0);
		j = nb / ++i;
	}
	return (1);
}
