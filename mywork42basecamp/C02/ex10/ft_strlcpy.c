unsigned int	ft_strlcpy(char *dest, char *src, unsigned int size)
{
	unsigned int	lengthsrc;
	unsigned int	i;

	lengthsrc = 0;
	i = 0;
	while (src[lengthsrc])
	{
		lengthsrc++;
	}
	if (size != 0)
	{
		while (src[i] && i < (size - 1))
		{
			dest[i] = src[i];
			i++;
		}
		dest[i] = '\0';
	}
	return (lengthsrc);
}
